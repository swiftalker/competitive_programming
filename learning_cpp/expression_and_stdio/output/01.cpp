#include <cstdio>

int main() {
    int a, b;

    printf("masukan nilai a: "); scanf("%d", &a);
    printf("masukan nilai b: "); scanf("%d", &b);
    printf("hasil dari penjumlahan a dan b adalah %d\n", a + b);
}

// stdio is standard input and output
// printf is print formatted
// scanf is scan formatted
// %d is integer
// %f is float
// %c is character
// %s is string
// %lf is double
// %x is hexadecimal
// %o is octal
// %u is unsigned
// %p is pointer
// %e is scientific notation
// %g is shorter of %f and %e
// %i is integer
// %u is unsigned
// %x is hexadecimal
// stdio have 2 streames, stdin for input and stdout for output